export const OQF_COMPLIANCE_CRITERIA = `
Based on the Oman Qualifications Framework (OQF) training materials, a Course Description (CDP) or syllabus must meet the following criteria for its learning outcomes:

1.  **Distinction Between Aims and Outcomes**: 
    - **Aims** should be broad statements that set out the intention and purpose of the course (teaching-centered).
    - **Learning Outcomes** must be detailed statements of what a learner is expected to know, do, or apply at the end of the course (learner-centered).

2.  **SMART Principles**: 
    - Outcomes must be **S**pecific, **M**easurable, **A**chievable, **R**ealistic, and **T**ime-bound.

3.  **Structural Integrity**: 
    - Each learning outcome should follow the structure: **Active Verb + Object + Qualifying Phrase**.
    - The **Active Verb** indicates what the learner is expected to do.
    - The **Object** specifies what the learner is to act on.
    - The **Qualifying Phrase** describes the context or conditions of performance.

4.  **Avoidance of Vague Verbs**: 
    - Do NOT use vague or unmeasurable verbs such as "know", "understand", "be aware of", or "be familiar with".
    - Use specific, assessable verbs (e.g., "analyze", "evaluate", "formulate", "list", "design").

5.  **Alignment and Coherence**: 
    - Outcomes must relate directly to the course title, aims, and OQF level descriptors.
    - They should be concise, coherent, and written in the future tense.

6.  **Manageability**: 
    - The number of learning outcomes should be manageable to ensure they can all be assessed within the given timeframe and resources.
`;
